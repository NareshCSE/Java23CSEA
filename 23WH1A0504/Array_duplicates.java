package javap;
import java.util.*;

public class Array_dup {
	
	public static void main(String[] args){
		Scanner n = new Scanner(System.in);
		int s = n.nextInt();
		int[] a = new int[s] ;
		
		for(int k=0;k<s;k++) {
			a[k] = n.nextInt();
		}
		
		for(int i=0;i<s;i++) {
			for(int j=1;j<i;j++) {
				if(a[i] == a[j]) {
					System.out.println(a[i]);
				}
			}
		}

	}

}
